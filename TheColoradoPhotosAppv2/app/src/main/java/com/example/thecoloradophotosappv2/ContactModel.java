package com.example.thecoloradophotosappv2;

public class ContactModel {
    private String title, link, thumbnail, date_taken, description, published, author, author_id, tags;

    public void setTitle(String title) {
        this.title = title;
    }

    public String getTitle() {
        return title;
    }

    public void setLink(String link) {
        this.link = link;
    }

    public String getLink() {
        return link;
    }

    public void setThumbnail(String thumbnail) {
        this.thumbnail = thumbnail;
    }

    public String getThumbnail() {
        return thumbnail;
    }

    public void setDateTaken(String date_taken) {
        this.date_taken = date_taken;
    }

    public String getDateTaken() {
        return date_taken;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getDescription() {
        return description;
    }

    public void setPublished(String published) {
        this.published = published;
    }

    public String getPublished() {
        return published;

    }

    public void setAuthor(String author) {
        this.author = author;
    }

    public String getAuthor() {
        return author;
    }

    public void setAuthorID(String author_id) {
        this.author_id = author_id;
    }

    public String getAuthorID() {
        return author_id;
    }

    public void setTags(String tags) {
        this.tags = tags;
    }

    public String getTags() {
        return tags;
    }
}

